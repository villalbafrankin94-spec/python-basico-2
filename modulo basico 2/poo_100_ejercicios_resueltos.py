# 51. [Avanzado] Encapsulación y properties 51

class Cuenta51:
    def __init__(self, saldo=0):
        self._saldo = saldo
    @property
    def saldo(self):
        return self._saldo
    def depositar(self, monto):
        if monto>0: self._saldo += monto
    def retirar(self, monto):
        if 0 < monto <= self._saldo:
            self._saldo -= monto
            return True
        return False

c = Cuenta51(100)
c.depositar(50)
c.retirar(30)
print(c.saldo)


